/**
 * Helper to get formatted knowledge for a specific agent
 */
export function getAgentKnowledge(role: 'analyst' | 'fundManager' | 'propTrader'): string {
    const data = KnowledgeVault[role];
    if (!data) return '';

    return `
【高度専門知識・思考フレームワーク】:
${data.frameworks.map(f => `- ${f}`).join('\n')}

【遵守すべきチェックリスト】:
${data.checklists.map(c => `- ${c}`).join('\n')}
`;
}
